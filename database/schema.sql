-- 1. DEPARTMENTS TABLE
-- Stores department details. MANAGER_ID references EMPLOYEES, which is added as a foreign key constraint later to avoid circular dependency.
CREATE TABLE DEPARTMENTS (
    DEPARTMENT_ID   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DEPARTMENT_NAME VARCHAR2(100) NOT NULL UNIQUE,
    MANAGER_ID      NUMBER, -- FK added later
    LOCATION        VARCHAR2(100) NOT NULL
);

-- 2. EMPLOYEES TABLE
-- Stores employee master information, including salaries and department links.
CREATE TABLE EMPLOYEES (
    EMPLOYEE_ID   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FIRST_NAME    VARCHAR2(50) NOT NULL,
    LAST_NAME     VARCHAR2(50) NOT NULL,
    EMAIL         VARCHAR2(100) NOT NULL UNIQUE,
    PHONE_NUMBER  VARCHAR2(20),
    HIRE_DATE     DATE DEFAULT SYSDATE NOT NULL,
    JOB_TITLE     VARCHAR2(100) NOT NULL,
    SALARY        NUMBER(12, 2) NOT NULL,
    DEPARTMENT_ID NUMBER,
    STATUS        VARCHAR2(20) DEFAULT 'Active' NOT NULL,
    CONSTRAINT FK_EMP_DEPT FOREIGN KEY (DEPARTMENT_ID) REFERENCES DEPARTMENTS(DEPARTMENT_ID) ON DELETE SET NULL,
    CONSTRAINT CHK_EMP_SALARY CHECK (SALARY > 0),
    CONSTRAINT CHK_EMP_STATUS CHECK (STATUS IN ('Active', 'Inactive', 'Suspended'))
);

-- ADD CIRCULAR FOREIGN KEY ON DEPARTMENTS (MANAGER_ID -> EMPLOYEES)
ALTER TABLE DEPARTMENTS ADD CONSTRAINT FK_DEPT_MGR FOREIGN KEY (MANAGER_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID) ON DELETE SET NULL;

-- 3. ATTENDANCE TABLE
-- Tracks daily attendance, check-in, check-out times, and calculates day status.
CREATE TABLE ATTENDANCE (
    ATTENDANCE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EMPLOYEE_ID   NUMBER NOT NULL,
    WORK_DATE     DATE NOT NULL,
    CHECK_IN      TIMESTAMP,
    CHECK_OUT     TIMESTAMP,
    STATUS        VARCHAR2(20) NOT NULL,
    CONSTRAINT FK_ATT_EMP FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID) ON DELETE CASCADE,
    CONSTRAINT CHK_ATT_STATUS CHECK (STATUS IN ('Present', 'Absent', 'Late', 'Half-Day', 'On Leave')),
    CONSTRAINT UQ_EMP_WORK_DATE UNIQUE (EMPLOYEE_ID, WORK_DATE)
);

-- 4. LEAVES TABLE
-- Stores leave applications, types, ranges, and approvals.
CREATE TABLE LEAVES (
    LEAVE_ID    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EMPLOYEE_ID NUMBER NOT NULL,
    LEAVE_TYPE  VARCHAR2(20) NOT NULL,
    START_DATE  DATE NOT NULL,
    END_DATE    DATE NOT NULL,
    STATUS      VARCHAR2(20) DEFAULT 'Pending' NOT NULL,
    REASON      VARCHAR2(255),
    CONSTRAINT FK_LV_EMP FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID) ON DELETE CASCADE,
    CONSTRAINT CHK_LV_TYPE CHECK (LEAVE_TYPE IN ('Sick', 'Casual', 'Annual', 'Unpaid')),
    CONSTRAINT CHK_LV_STATUS CHECK (STATUS IN ('Pending', 'Approved', 'Rejected')),
    CONSTRAINT CHK_LV_DATES CHECK (END_DATE >= START_DATE)
);

-- 5. PAYROLL TABLE
-- Stores processed payroll statements for each employee and period.
CREATE TABLE PAYROLL (
    PAYROLL_ID        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EMPLOYEE_ID       NUMBER NOT NULL,
    PAY_PERIOD_START  DATE NOT NULL,
    PAY_PERIOD_END    DATE NOT NULL,
    BASIC_SALARY      NUMBER(12, 2) NOT NULL,
    ALLOWANCES        NUMBER(12, 2) DEFAULT 0 NOT NULL,
    DEDUCTIONS        NUMBER(12, 2) DEFAULT 0 NOT NULL,
    TAX               NUMBER(12, 2) DEFAULT 0 NOT NULL,
    NET_SALARY        NUMBER(12, 2) NOT NULL,
    PAYMENT_DATE      DATE,
    STATUS            VARCHAR2(20) DEFAULT 'Draft' NOT NULL,
    CONSTRAINT FK_PAY_EMP FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID) ON DELETE CASCADE,
    CONSTRAINT CHK_PAY_STATUS CHECK (STATUS IN ('Draft', 'Paid', 'Voided')),
    CONSTRAINT CHK_PAY_DATES CHECK (PAY_PERIOD_END >= PAY_PERIOD_START),
    CONSTRAINT CHK_PAY_NET CHECK (NET_SALARY >= 0)
);

-- Indexes on Foreign Keys to optimize joins and search queries
CREATE INDEX IX_EMP_DEPT ON EMPLOYEES(DEPARTMENT_ID);
CREATE INDEX IX_ATT_EMP ON ATTENDANCE(EMPLOYEE_ID);
CREATE INDEX IX_LV_EMP ON LEAVES(EMPLOYEE_ID);
CREATE INDEX IX_PAY_EMP ON PAYROLL(EMPLOYEE_ID);

-- Comments to describe columns (standard Oracle practice)
COMMENT ON TABLE EMPLOYEES IS 'Master table storing employee information';
COMMENT ON COLUMN EMPLOYEES.STATUS IS 'Active, Inactive, or Suspended';
COMMENT ON TABLE ATTENDANCE IS 'Daily attendance tracking log';
COMMENT ON TABLE LEAVES IS 'Employee leave requests and approvals';
COMMENT ON TABLE PAYROLL IS 'Generated payslip details per pay period';
